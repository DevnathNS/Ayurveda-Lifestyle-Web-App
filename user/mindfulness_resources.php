        <div class="row">
            <div class="col-md-6">
                <h5>Mindful Walking</h5>
                <p>Take a walk outside and focus your attention on each step you take. 
                Notice the sensation of your feet making contact with the ground, the movement of your muscles, and the sounds and sights around you. 
                Stay present in the moment, letting go of any distractions or worries.</p>         
            </div>
            <div class="col-md-6">
                <h5>Mindful Eating</h5>
                <p>Before eating a meal or snack, take a moment to appreciate the food in front of you. 
                Notice the colors, textures, and smells of your food. Take small bites and chew slowly, paying attention to the taste and sensations in your mouth. 
                Be fully present with each bite, savoring the experience of eating.</p>
            </div>
        </div>
        <div class="row">
            <div class="col-md-6">
                <h5>Gratitude Practice</h5>
                <p>Take time each day to reflect on things you're grateful for. 
                    It could be something as simple as a beautiful sunrise, a kind gesture from a friend, or a delicious meal. 
                    Write down three things you're grateful for each day or simply take a moment to silently acknowledge them.</p>         
            </div>
            <div class="col-md-6">
                <h5>Mindful Listening</h5>
                <p>Engage in a conversation with someone and practice active listening. 
                    Give the speaker your full attention without interrupting or formulating your response in your mind. 
                    Notice the tone of their voice, their body language, and the emotions behind their words.</p>
            </div>
        </div>
        <div class="row">
            <div class="col-md-6">
                <h5>Mindful Journaling</h5>
                <p>Set aside time each day to write in a journal. 
                    Reflect on your thoughts, feelings, and experiences without judgment. 
                    Notice any patterns or recurring themes in your writing and use it as an opportunity for self-awareness and growth.</p>         
            </div>
            <div class="col-md-6">
                <h5>Loving-Kindness Meditation</h5>
                <p>Sit quietly and repeat phrases of loving-kindness to yourself and others. 
                    Start with phrases like "May I be happy, may I be healthy, may I be safe, may I live with ease." 
                    Then extend these wishes to loved ones, acquaintances, and even people you may have difficulty with.</p>
