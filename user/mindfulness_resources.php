                <h5>Body Scan Meditation</h5>
                <p>Sit or lie down in a comfortable position. 
                Start by bringing your awareness to your feet and slowly move your attention up through your body, noticing any sensations or areas of tension. 
                Pay attention to each part of your body without judgment, allowing yourself to relax and release any tension you may be holding.</p>
            </div>
